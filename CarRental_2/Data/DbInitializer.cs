        private static string GenerateRandomLetters(Random random)
        {
            const string letters = "ABCDEFGHIJKLMNOPQRSTUVWXYZ";
            return $"{letters[random.Next(0, 26)]}{letters[random.Next(0, 26)]}";
        }

        private static string GetRandomElement(string[] array)
        {
            return array[rand.Next(array.Length)];
        }

        private static string GenerateRandomLicenseNumber()
        {
            int length = rand.Next(8, 10);
            const string symbols = "ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789";
            return new string(Enumerable.Repeat(symbols, length)
                .Select(s => s[rand.Next(s.Length)]).ToArray());
        }

        private static string GenerateRandomPhoneNumber()
        {
            return $"{rand.Next(100000, 999999)}";
        }
